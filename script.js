console.clear();
// JavaScript:  Switch Statement

let vowels = prompt('Enter  a character', 'Enter...');
switch (vowels) {
    case 'a':
        console.log('A is vowel!');
        break;
    case 'e':
        console.log('E is vowel!');
        break;
    case 'i':
        console.log('I is vowel!');
        break;
    case 'o':
        console.log('O is vowel!');
        break;
    case 'u':
        console.log('U is vowel!');
        break;
    default:
        console.log('Consonant!');
        break;
}
